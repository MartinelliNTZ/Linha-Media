from qgis.PyQt.QtCore import QCoreApplication, QVariant
from qgis.core import (QgsProcessing,
                       QgsFeatureSink,
                       QgsProcessingAlgorithm,
                       QgsProcessingParameterFeatureSource,
                       QgsProcessingParameterFeatureSink,
                       QgsProcessingParameterNumber,
                       QgsProcessingParameterEnum,
                       QgsProcessingException,
                       QgsGeometry,
                       QgsFeature,
                       QgsWkbTypes,
                       QgsFields,
                       QgsField)
from .vector_utils import VectorUtils
from .connection_judge import ConnectionJudge

class LinhaMestraAlgorithm(QgsProcessingAlgorithm):
    """
    This is an example algorithm that takes a vector layer and
    creates a new identical one.

    It is meant to be used as an example of how to create your own
    algorithms and explain methods and variables used to do it. An
    algorithm like this will be available in all elements, and there
    is not need for additional work.

    All Processing algorithms should extend the QgsProcessingAlgorithm
    class.
    """

    # Constants used to refer to parameters and outputs. They will be
    # used when calling the algorithm from another algorithm, or when
    # calling from the QGIS console.

    OUTPUT = 'OUTPUT'
    INPUT = 'INPUT'
    INPUT_2 = 'INPUT_2'
    CONEXAO_OUTPUT = 'CONEXAO_OUTPUT'
    CRITERIO_PROXIMIDADE = 'CRITERIO_PROXIMIDADE'
    ESTILO_CONEXAO = 'ESTILO_CONEXAO'
    PARTICOES = 'PARTICOES'

    def initAlgorithm(self, config):
        self.addParameter(
            QgsProcessingParameterFeatureSource(
                self.INPUT,
                self.tr('Primeira Camada (ou camada com as 2 linhas)'),
                [QgsProcessing.TypeVectorLine]
            )
        )

        self.addParameter(
            QgsProcessingParameterFeatureSource(
                self.INPUT_2,
                self.tr('Segunda Camada (Opcional - para 1 feição cada)'),
                [QgsProcessing.TypeVectorLine],
                optional=True
            )
        )

        self.addParameter(
            QgsProcessingParameterNumber(
                self.PARTICOES,
                self.tr('Número de Partiçôes'),
                type=QgsProcessingParameterNumber.Integer,
                minValue=10,
                maxValue=999999,
                defaultValue=1000
            )
        )

        self.addParameter(
            QgsProcessingParameterEnum(
                self.ESTILO_CONEXAO,
                self.tr('Estilo de Conexão'),
                options=['Proximidade', 'Perpendicular', 'Conexão Direta (Ponto a Ponto)'],
                defaultValue=0
            )
        )

        self.addParameter(
            QgsProcessingParameterEnum(
                self.CRITERIO_PROXIMIDADE,
                self.tr('Critério de Proximidade (Escolha da Base)'),
                options=['Menor Tamanho', 'Maior Tamanho', 'Menor Ângulo', 'Maior Ângulo', 'Qualquer uma'],
                defaultValue=0
            )
        )

        # We add a feature sink in which to store our processed features (this
        # usually takes the form of a newly created vector layer when the
        # algorithm is run in QGIS).
        self.addParameter(
            QgsProcessingParameterFeatureSink(
                self.OUTPUT,
                self.tr('Linha Mestra'),
                QgsProcessing.TypeVectorLine
            )
        )

        self.addParameter(
            QgsProcessingParameterFeatureSink(
                self.CONEXAO_OUTPUT,
                self.tr('Conexões'),
                QgsProcessing.TypeVectorLine
            )
        )

    def processAlgorithm(self, parameters, context, feedback):
        source = self.parameterAsSource(parameters, self.INPUT, context)
        source2 = self.parameterAsSource(parameters, self.INPUT_2, context)
        particoes = self.parameterAsInt(parameters, self.PARTICOES, context)
        criterio = self.parameterAsInt(parameters, self.CRITERIO_PROXIMIDADE, context)
        estilo = self.parameterAsInt(parameters, self.ESTILO_CONEXAO, context)

        # 1. Extração e Validação (Delegado para VectorUtils)
        linha1, linha2, crs_final = VectorUtils.extract_two_features(source, source2, context)
        if linha1 is None:
            raise QgsProcessingException(self.tr('Erro de Seleção: Escolha 2 feições na mesma camada ou 1 em cada camada.'))

        # 2. Processamento Geométrico Pesado (Delegado para VectorUtils)
        mestra_results, conexao_results, perp_results = VectorUtils.generate_linhamestra_elements(
            linha1.geometry(), linha2.geometry(), particoes, feedback
        )

        # 2.1. Seleção do estilo de conexão conforme parâmetro
        if estilo == 0: # Proximidade
            conexao_final_results = ConnectionJudge.solve_nearest_with_criteria(
                linha1.geometry(), linha2.geometry(), criterio)
        elif estilo == 1: # Perpendicular
            conexao_final_results = perp_results
        else: # Conexão Direta
            conexao_final_results = conexao_results

        # Definir os campos de saída (ID do segmento)
        fields_mestra = QgsFields()
        fields_mestra.append(QgsField('id_segmento', QVariant.Int))
        fields_mestra.append(QgsField('dist_mae', QVariant.Double))
        
        (sink, dest_id) = self.parameterAsSink(
            parameters, 
            self.OUTPUT,
            context, 
            fields_mestra, 
            QgsWkbTypes.LineString, 
            crs_final # CRS da primeira camada
        )

        # Configurar o sink para as conexões (Unificado)
        fields_conexao = QgsFields()
        fields_conexao.append(QgsField('id_conexao', QVariant.Int))
        (sink_conexao, dest_id_conexao) = self.parameterAsSink(
            parameters,
            self.CONEXAO_OUTPUT,
            context,
            fields_conexao,
            QgsWkbTypes.LineString,
            crs_final
        )

        # 3. Escrita dos Resultados (Orquestração pura)
        feedback.pushInfo(self.tr('Escrevendo feições na camada de saída...'))

        def write_results(results, sink, fields):
            for res in results:
                if feedback.isCanceled(): break
                feat = VectorUtils.create_feature(res['geom'], fields, [res['id']])
                sink.addFeature(feat, QgsFeatureSink.FastInsert)

        # Mestra precisa de um campo extra (dist_mae), então fazemos manual
        for i, res in enumerate(mestra_results):
            if feedback.isCanceled(): break
            feat = VectorUtils.create_feature(res['geom'], fields_mestra, [res['id'], res['dist']])
            sink.addFeature(feat, QgsFeatureSink.FastInsert)

        write_results(conexao_final_results, sink_conexao, fields_conexao)

        return {
            self.OUTPUT: dest_id, 
            self.CONEXAO_OUTPUT: dest_id_conexao
        }

    def name(self):
        return 'linhamestra_gerador'

    def displayName(self):
        return self.tr('Gerador de Linha Mestra')

    def group(self):
        return self.tr('Linha Mestra')

    def groupId(self):
        return 'linhamestra'

    def tr(self, string):
        return QCoreApplication.translate('LinhaMestraAlgorithm', string)

    def createInstance(self):
        return LinhaMestraAlgorithm()
